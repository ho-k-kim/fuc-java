package exercicios3_java_aula_2_parte_1;

public class App {

	public static void main(String[] args) {
		/*
		 * Crie um programa que use um loop for para imprimir uma contagem regressiva de 10 até 1.
			Ao final, exiba a mensagem "Fogo!".
		 * 
		 */

		for(int i=10;i>0;i--) {
			System.out.println(i);
		}
		System.out.println("Fogo!\n");
	}

}
